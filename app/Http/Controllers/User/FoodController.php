    {
        $form_data =$request->all();
        $food = new Food();
        
       
        $slug = Str::slug($form_data['name'],'-');
        $form_data['slug']= $slug;
        $food->fill($form_data);
        $food-> save();

        
        return redirect()->route('user.foods.index');
    }

